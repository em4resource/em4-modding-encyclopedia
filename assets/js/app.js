let pendingArticleTitle = null;

function articleMatches(article, query) {
  if (article.category === "GitHub") {
    return false;
  }

  if (!query && article.category !== activeCategory) {
    return false;
  }

  if (!query) {
    return true;
  }

  const haystack = [
    article.category,
    article.title,
    article.tags.join(" "),
    article.body.replace(/<[^>]+>/g, " ")
  ].join(" ").toLowerCase();

  return haystack.includes(query.toLowerCase());
}

function renderCategories() {
  categoryList.innerHTML = "";

  categoryGroups.forEach(group => {
    const visibleCategories = group.categories.filter(category => categories.includes(category));
    if (visibleCategories.length === 0) {
      return;
    }

    const section = document.createElement("div");
    section.className = "category-group";

    const title = document.createElement("div");
    title.className = "category-group-title";
    title.textContent = group.title;
    section.appendChild(title);

    visibleCategories.forEach(category => {
      const button = document.createElement("button");
      button.type = "button";
      button.className = `menu-button${category === activeCategory ? " active" : ""}`;
      if (category === "Discord Rules") {
        const icon = document.createElement("span");
        icon.className = "menu-icon discord-menu-icon";
        icon.setAttribute("aria-hidden", "true");
        icon.textContent = "D";

        const label = document.createElement("span");
        label.textContent = category;

        button.appendChild(icon);
        button.appendChild(label);
      } else {
        button.textContent = category;
      }
      button.addEventListener("click", () => {
        activeCategory = category;
        renderCategories();
        renderArticles();
      });
      section.appendChild(button);
    });

    categoryList.appendChild(section);
  });

  categories
    .filter(category => !categoryGroups.some(group => group.categories.includes(category)))
    .forEach(category => {
      const button = document.createElement("button");
      button.type = "button";
      button.className = `menu-button${category === activeCategory ? " active" : ""}`;
      button.textContent = category;
      button.addEventListener("click", () => {
        activeCategory = category;
        renderCategories();
        renderArticles();
      });
      categoryList.appendChild(button);
  });
}

function renderArticles() {
  const query = searchBox.value.trim();
  const visible = articles.filter(article => articleMatches(article, query));
  let openedPendingArticle = false;

  articleGrid.innerHTML = "";
  articleTitle.textContent = query ? "Search Results" : activeCategory;
  articleCounter.textContent = `${visible.length} entr${visible.length === 1 ? "y" : "ies"}`;

  visible.forEach((article, index) => {
    const node = template.content.cloneNode(true);
    const card = node.querySelector(".article-card");
    const title = node.querySelector(".article-title");
    const body = node.querySelector(".article-body");
    const shouldOpenPending = pendingArticleTitle && article.title === pendingArticleTitle;

    title.textContent = article.title;
    const renderBody = () => {
      if (body.dataset.loaded === "true") {
        return;
      }
      body.innerHTML = `${article.body}<div class="tag-row">${article.tags.map(tag => `<span class="tag">${tag}</span>`).join("")}</div>`;
      body.dataset.loaded = "true";
    };

    if ((index === 0 && !pendingArticleTitle) || shouldOpenPending) {
      card.classList.add("open");
      renderBody();
      openedPendingArticle = openedPendingArticle || shouldOpenPending;
    }

    title.addEventListener("click", () => {
      card.classList.toggle("open");
      if (card.classList.contains("open")) {
        renderBody();
      }
    });

    articleGrid.appendChild(node);
  });

  if (openedPendingArticle) {
    pendingArticleTitle = null;
  }
}

searchBox.addEventListener("input", renderArticles);

